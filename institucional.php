                <h2>Breve reseña histórica</h2>

                <p>La Institución Educativa Regional Simón Bolívar se encuentra ubicada en el corregimiento San Antonio de los Caballeros, del municipio de Florida Valle, al frente del Centro Nacional de Investigación de la Caña de Azúcar CENICAÑA.</p>

                <p>En el año 1965, el señor Jaime Domínguez Vásquez donó el terreno para que se construyera el plantel. La construcción se realizó con dineros provenientes del Estado y de donaciones de habitantes de la comunidad. Inició labores en el año 1969 como satélite del colegio Germán Nieto de Candelaria.</p>

                <p>Su primer rector entre los años 1969 y 1971 fue el señor Raúl Abella. La primera promoción de bachilleres graduandos a nombre de este plantel fue en el año 1973, siendo rector el Lic. Nelson Díaz Nieto y Coordinador, el Lic. Óscar Ocampo.</p>

                <p>Debido a que la principal fuente económica de esta región es la agroindustria azucarera, y en menor escala, los cultivos de pancoger; se decidió que el plantel tuviera la modalidad agropecuaria. Desde su inicio, y hasta el año 1972, este plantel sólo ofrecía educación básica secundaria; es decir, los grados 1°, 2°, 3° y 4° de bachillerato, equivalentes a los grados de 6° a 9° de la actualidad. El nivel de educación media, o sea los grados 5° y 6° se ofrecieron en convenio con el colegio Germán Nieto del municipio de Candelaria y el Cárdenas de Palmira.</p>

                <p>En el año 2002 por directriz del Ministerio de Educación Nacional se implementan las fusiones de escuelas y colegios, en una sola institución; convirtiéndose en la Institución Educativa Regional Simón Bolívar, conformada por tres sedes que son las escuelas Simón Bolívar y Parroquial, y el colegio Gimnasio Regional Simón Bolívar.</p>


                <h2>Misión</h2>

                <p>
                La Institución Educativa Regional Simón Bolívar, ofrece servicios educativos en el corregimiento de San Antonio de los Caballeros, perteneciente al municipio de Florida (V).
                </p>

                <p>
                Nuestro compromiso es ofrecer servicios educativos orientados por principios de formación integral y de inclusión social (P.E.C), enmarcados en la modalidad técnica agropecuaria desde los niveles de transición a undécimo grado de educación secundaria, media técnica y ciclos especiales académicos con el objetivo de fortalecer el desarrollo integral (Medio Ambiente) de la persona, al profundizar y materializar la relación de las áreas del saber con la identidad pluricultural y la vida de la población del corregimiento de San Antonio de los Caballeros.
                </p>

                <p>
                Gracias a la implementación de la modalidad técnica agropecuaria orientamos en nuestros estudiantes en el liderazgo y las competencias pertinentes para promover una formación académica, laboral y en valores que le facilitan vincularse efectivamente en el sector productivo.
                </p>

                <h2>Visión</h2>

                <p>
                Al 2025 la Institución Educativa Regional Simón Bolívar será reconocida en la región por la calidad de sus procesos académicos, agropecuarios y tecnológicos enmarcados en un ambiente inclusivo y de sana convivencia.
                </p>

                <p>
                    Nuestra estrategia educativa fortalece el desarrollo artístico, deportivo, cultural y a su vez, propicia la formación de estudiantes capaces de liderar cambios positivos en la comunidad, para el mejoramiento de la calidad de vida de los habitantes del corregimiento de San Antonio de los Caballeros, implementando una producción agropecuaria sostenible y limpia que se apoye en los adelantos científicos y tecnológicos (T.IC), además, reconociendo la importancia de una segunda lengua se busca el mejoramiento de los niveles de la lengua inglesa (bilingüismo) para una mejor comunicación e interacción con el mundo globalizado.
                </p>

